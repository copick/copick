# Filesystem application
# --------------------------------------------------------------------------- #
def _write_if_changed(path: str, content: str) -> bool:
    if os.path.exists(path):
        with open(path) as fh:
            if fh.read() == content:
                return False
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w") as fh:
        fh.write(content)
    return True


def _prune_stale(outdir: str, valid: set) -> List[str]:
    """Remove autogenerated pages under ``outdir`` that no longer correspond to a command."""
    removed = []
    for root, _dirs, files in os.walk(outdir):
        for fname in files:
            if not fname.endswith(".md"):
                continue
            full = os.path.join(root, fname)
            if full in valid:
                continue
            with open(full) as fh:
                head = fh.read(120)
            if head.startswith("<!-- AUTOGENERATED"):
                os.remove(full)
                removed.append(full)
    return removed


@click.command(context_settings={"show_default": True})
@click.option(
    "--outdir",
    type=click.Path(file_okay=False),
    default="docs/cli",
    help="Output directory for generated CLI pages.",
)
@click.option(
    "--config",
    "config_path",
    type=click.Path(dir_okay=False),
    default="zensical.toml",
    help="Zensical config whose CLI-Reference nav node is rewritten.",
)
@click.option(
    "--check",
    is_flag=True,
    default=False,
    help="Verify pages/nav are up to date; exit non-zero on drift (no writes).",
)
@click.option("--prune/--no-prune", default=True, help="Remove stale autogenerated pages with no matching command.")
def main(outdir: str, config_path: str, check: bool, prune: bool) -> None:
    """Generate the per-command CLI reference from the assembled copick CLI."""
    pages = {**_generate_pages(outdir), **_generate_gallery()}

    if check:
        drift: List[str] = []
        for path, content in pages.items():
            if not os.path.exists(path):
                drift.append(f"missing: {path}")
            else:
                with open(path) as fh:
                    if fh.read() != content:
                        drift.append(f"changed: {path}")
        nav_changed, _ = _rewrite_nav(config_path, write=False)
        if nav_changed:
            drift.append(f"changed: {config_path} (nav)")
        if drift:
            logger.error("CLI docs are out of date. Run `make_cli_docs`.")
            for item in drift[:50]:
                logger.error("  %s", item)
            raise SystemExit(1)
        logger.info("CLI docs are up to date (%d pages).", len(pages))
        return

    written = sum(_write_if_changed(path, content) for path, content in pages.items())
    removed = _prune_stale(outdir, set(pages)) if prune else []
    nav_changed, _ = _rewrite_nav(config_path, write=True)

    logger.info(
        "Generated %d pages (%d updated), incl. the processing-tools gallery, pruned %d stale, nav %s.",
        len(pages),
        written,
        len(removed),
        "rewritten" if nav_changed else "unchanged",
    )


if __name__ == "__main__":
    main()
