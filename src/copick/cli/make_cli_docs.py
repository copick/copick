def _nav_subtree() -> list:
    """Build the CLI-Reference nav subtree as plain dict/list, grouped by category."""
    ctx = click.Context(cli, info_name="copick")
    top = {name: cli.get_command(ctx, name) for name in cli.list_commands(ctx)}
    top = {k: v for k, v in top.items() if v is not None and not v.hidden}

    def entry(name: str, cmd: click.Command, path: List[str]) -> dict:
        full = path + [name]
        if _is_group(cmd):
            children: list = [{"Overview": "cli/" + "/".join(full) + "/index.md"}]
            for sub in cmd.list_commands(ctx):
                subcmd = cmd.get_command(ctx, sub)
                if subcmd is None or subcmd.hidden:
                    continue
                children.append(entry(sub, subcmd, full))
            return {name: children}
        return {name: "cli/" + "/".join(full) + ".md"}

    nav: list = [{"Overview": "cli/index.md"}]
    seen = set()
    for category, names in COMMAND_CATEGORIES.items():
        present = [n for n in sorted(names) if n in top]
        if not present:
            continue
        seen.update(present)
        nav.append({category: [entry(n, top[n], []) for n in present]})
    other = [n for n in sorted(top) if n not in seen]
    if other:
        nav.append({"Other Commands": [entry(n, top[n], []) for n in other]})
    return nav


def _fmt_nav_element(key: str, value, indent: int) -> str:
    """Serialise one nav element as a TOML inline table, matching the file's style.

    Produces the same progressively-indented, fully-quoted-key style as the
    hand-written nav entries (4 spaces per level).
    """
    pad = "    " * indent
    if isinstance(value, list):
        lines = [f'{pad}{{"{key}" = [']
        for el in value:
            ((child_key, child_val),) = el.items()
            lines.append(_fmt_nav_element(child_key, child_val, indent + 1) + ",")
        lines.append(f"{pad}]}}")
        return "\n".join(lines)
    return f'{pad}{{"{key}" = "{value}"}}'


def _find_cli_ref_span(text: str) -> Optional[Tuple[int, int]]:
    """Return (start, end) char offsets of the ``{"CLI Reference" = ...}`` nav element.

    Brace-balanced scan; paths/titles in the nav contain no braces so this is
    unambiguous. Works whether the element is the original ``"cli.md"`` string or
    a previously-generated nested block (idempotent).
    """
    match = re.search(r'\{\s*"CLI Reference"\s*=', text)
    if not match:
        return None
    start = match.start()
    depth = 0
    in_str = False
    for i in range(start, len(text)):
        ch = text[i]
        if ch == '"' and text[i - 1] != "\\":
            in_str = not in_str
        elif not in_str:
            if ch == "{":
                depth += 1
            elif ch == "}":
                depth -= 1
                if depth == 0:
                    return start, i + 1
    return None


def _rewrite_nav(config_path: str, write: bool) -> Tuple[bool, str]:
    """Rewrite only the ``CLI Reference`` element of the nav array. Returns (changed, new_text)."""
    with open(config_path) as fh:
        original = fh.read()

    span = _find_cli_ref_span(original)
    if span is None:
        logger.warning("No 'CLI Reference' nav element in %s; skipping nav rewrite.", config_path)
        return False, original

    element = _fmt_nav_element("CLI Reference", _nav_subtree(), indent=1).lstrip()
    start, end = span
    new_text = original[:start] + element + original[end:]

    # Safety net: never write a config we cannot parse.
    try:
        import tomllib

        tomllib.loads(new_text)
    except ImportError:
        pass

    changed = new_text != original
    if changed and write:
        with open(config_path, "w") as fh:
            fh.write(new_text)
    return changed, new_text


