# Per-tool thumbnails are dropped in by maintainers at
# ``docs/assets/tools/<group>/<command>.<ext>`` (any of the extensions below).
# Until one exists, the shared placeholder is used; no code change is needed to
# swap a real render in — just add the file and regenerate.
_TOOL_IMAGE_EXTS = ("png", "webp", "jpg", "jpeg", "svg")
_PLACEHOLDER_IMAGE = "assets/tools/_placeholder.svg"


def _tool_image(group: str, command: str) -> str:
    """Page-relative thumbnail path for a tool, falling back to the placeholder."""
    for ext in _TOOL_IMAGE_EXTS:
        if os.path.exists(os.path.join("docs", "assets", "tools", group, f"{command}.{ext}")):
            return f"assets/tools/{group}/{command}.{ext}"
    return _PLACEHOLDER_IMAGE


def _group_blurb(ctx: click.Context, group: str) -> str:
    grp = cli.get_command(ctx, group)
    if grp is None:
        return ""
    doc = inspect.getdoc(grp.callback) if grp.callback else None
    if doc:
        return doc.strip().split("\n\n")[0].replace("\n", " ")
    return grp.short_help or ""


def _group_tools(ctx: click.Context, group: str) -> List[Tuple[str, str]]:
    """Sorted (command_name, synopsis) for the leaf tools of a CLI group."""
    grp = cli.get_command(ctx, group)
    if grp is None:
        return []
    out = []
    for name in grp.list_commands(ctx):
        cmd = grp.get_command(ctx, name)
        if cmd is None or cmd.hidden:
            continue
        out.append((name, cmd.short_help or cmd.get_short_help_str() or ""))
    return out


def _tool_card(group: str, name: str, synopsis: str) -> str:
    img = _tool_image(group, name)
    link = f"cli/{group}/{name}.md"
    return (
        f"-   [![{name}]({img}){{ .tool-thumb }}]({link})\n\n"
        f"    **{name}**\n\n"
        f"    {synopsis}\n\n"
        f"    [:octicons-arrow-right-24: Details]({link})"
    )


def _render_gallery() -> str:
    ctx = click.Context(cli, info_name="copick")
    blocks = [
        AUTOGEN_HEADER,
        "# Processing Tools\n",
        "*Transform cryoET data from the command line — convert between picks, segmentations, "
        "and meshes; process volumes and segmentations; and run spatial / boolean operations. "
        "Every tool below is a `copick` CLI command; click through for full usage and examples.*\n",
    ]
    if os.path.exists("docs/cli_editorial/processing_tools.md"):
        blocks.append('--8<-- "docs/cli_editorial/processing_tools.md"\n')

    for group, title in GALLERY_GROUPS:
        tools = _group_tools(ctx, group)
        if not tools:
            continue
        blocks.append(f"## {title}\n")
        blurb = _group_blurb(ctx, group)
        if blurb:
            blocks.append(f"{blurb}\n")
        cards = "\n\n".join(_tool_card(group, name, syn) for name, syn in tools)
        blocks.append('<div class="grid cards" markdown>\n\n' + cards + "\n\n</div>\n")

    return "\n".join(b for b in blocks if b).rstrip() + "\n"


# Seconds the marquee takes to traverse one card; the full loop duration scales
# with card count so every carousel auto-scrolls at the same gentle speed.
_MARQUEE_SECONDS_PER_CARD = 6


def _marquee(cards: List[str]) -> str:
    """Wrap card markdown into an infinite, auto-scrolling marquee.

    Two identical card groups sit in a flex track; translating the track by one
    group width loops seamlessly. The second group is ``aria-hidden`` (a visual
    duplicate). Loop duration scales with the card count for a constant speed.
    The CSS (``.tool-marquee*`` in extra.css) pauses on hover/focus and falls back
    to a manual scroll strip under ``prefers-reduced-motion``.
    """
    group = "\n\n".join(cards)
    duration = max(20, len(cards) * _MARQUEE_SECONDS_PER_CARD)
    return (
        '<div class="tool-marquee" markdown>\n'
        f'<div class="tool-marquee__track" markdown style="--marquee-duration: {duration}s">\n\n'
        f'<div class="tool-marquee__group" markdown>\n\n{group}\n\n</div>\n\n'
        f'<div class="tool-marquee__group" markdown aria-hidden="true">\n\n{group}\n\n</div>\n\n'
        "</div>\n</div>"
    )


def _render_carousel() -> str:
    ctx = click.Context(cli, info_name="copick")
    items = []
    for group, name in CAROUSEL_HIGHLIGHTS:
        grp = cli.get_command(ctx, group)
        cmd = grp.get_command(ctx, name) if grp is not None else None
        if cmd is None or cmd.hidden:
            continue
        synopsis = cmd.short_help or cmd.get_short_help_str() or ""
        img = _tool_image(group, name)
        link = f"cli/{group}/{name}.md"
        items.append(
            f"-   [![{name}]({img}){{ .tool-thumb }}]({link})\n\n    **[{name}]({link})**\n\n    {synopsis}",
        )
    return (
        "<!-- AUTOGENERATED by `make_cli_docs`. Do not edit by hand. -->\n\n"
        + _marquee(items)
        + "\n\n[:octicons-arrow-right-24: Browse all processing tools](processing_tools.md)\n"
    )


