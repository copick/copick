"""Generate per-command CLI reference pages from the assembled copick CLI.

This walks the fully-assembled ``copick`` Click group (core commands plus every
plugin command contributed by copick-utils, copick-torch, copick-mcp, ...) and
renders one Markdown page per command into ``docs/cli/``. Command ``--help`` /
docstrings are the single source of truth: descriptions, ``Arguments:``,
``URI Format:``, ``Examples:``, ``See Also:`` and ``Notes:`` sections come from
the docstring, while the option/argument tables are built from the live Click
parameter objects so they can never drift from the actual CLI.

A generated page can carry hand-written editorial content (figures, extended
prose, tips): create ``docs/cli_editorial/<.../command>.md`` and regenerate, and
the page gains a ``pymdownx.snippets`` include of it at the bottom. Editorial
partials are authored by hand and never written or overwritten by the generator,
so they survive regeneration; pages without one simply omit the include.

The generator also rewrites *only* the ``CLI Reference`` element of the
``nav`` array in ``zensical.toml`` (a targeted text splice that preserves the
rest of the file byte-for-byte), grouped by the same ``COMMAND_CATEGORIES`` used
for ``copick --help``.

It also emits a visual **Processing Tools gallery** (``docs/processing_tools.md``)
and a carousel teaser snippet (``docs/snippets/processing_carousel.snippet``)
from the same introspection — one card per convert/process/logical command, with
a thumbnail resolved from ``docs/assets/tools/<group>/<command>.<ext>`` (falling
back to a shared placeholder until a real render is dropped in).

Run ``make_cli_docs`` to (re)generate, or ``make_cli_docs --check`` to verify
the committed docs are up to date (used in CI / pre-commit).
"""

import inspect
import os
import re
import textwrap
from typing import Dict, List, Optional, Tuple

import click

from copick.cli.cli import COMMAND_CATEGORIES, cli
from copick.util.log import get_logger

logger = get_logger(__name__)

AUTOGEN_HEADER = (
    "<!-- AUTOGENERATED by `make_cli_docs` (copick.cli.make_cli_docs). Do not edit by hand.\n"
    "     Editorial additions go in the matching docs/cli_editorial/ partial. -->\n"
)

# Recognised docstring section headers (a closed vocabulary, matched at column 0
# after dedent). "Example" and "Examples" are both accepted.
_SECTION_RE = re.compile(r"^(Arguments|URI Format|Examples?|See Also|Notes):\s*$")


# --------------------------------------------------------------------------- #
# Click introspection helpers
# --------------------------------------------------------------------------- #
def _is_group(obj: click.Command) -> bool:
    return isinstance(obj, click.Group)


def _group_of(param: click.Parameter):
    """Return the ``click_option_group`` OptionGroup a param belongs to, or None."""
    for key, value in vars(param).items():
        if key.endswith("__group"):
            return value
    return None


def _is_group_title(param: click.Parameter) -> bool:
    return type(param).__name__ == "_GroupTitleFakeOption"


def _arg_metavar(cmd: click.Command, arg: click.Argument, parent_ctx: click.Context) -> str:
    """Best-effort metavar for a positional argument (e.g. ``PATH`` or ``{a|b}``)."""
    if arg.metavar:
        return arg.metavar
    try:
        child = click.Context(cmd, info_name=cmd.name, parent=parent_ctx)
        return arg.make_metavar(child)
    except Exception:
        try:
            return arg.make_metavar()
        except Exception:
            return arg.name.upper()


def _type_label(param: click.Parameter) -> str:
    """Human-readable type for an option, e.g. ``choice (x | y | z)`` or ``integer``."""
    if getattr(param, "is_flag", False):
        label = "boolean flag"
    else:
        tname = getattr(param.type, "name", "text")
        choices = getattr(param.type, "choices", None)
        label = "choice (" + " \\| ".join(str(c) for c in choices) + ")" if choices else tname
    if getattr(param, "multiple", False):
        label += " · multiple"
    return label


def _is_unset(value) -> bool:
    # Click 8.2 uses a ``Sentinel.UNSET`` enum member for "no default".
    return value is None or type(value).__name__ == "Sentinel"


def _default_label(param: click.Parameter) -> str:
    if getattr(param, "required", False):
        return "**required**"
    default = getattr(param, "default", None)
    if _is_unset(default):
        return "—"
    if isinstance(default, str):
        return f'`"{default}"`' if default == "" else f"`{default}`"
    if isinstance(default, (list, tuple)) and len(default) == 0:
        return "—"
    return f"`{default}`"


def _opt_names(param: click.Parameter) -> str:
    primary = ", ".join(param.opts)
    if getattr(param, "secondary_opts", None):
        primary += " / " + ", ".join(param.secondary_opts)
    return f"`{primary}`"


def _cell(text: Optional[str]) -> str:
    """Sanitise a help string for a Markdown table cell."""
    if not text:
        return ""
    return " ".join(text.split()).replace("|", "\\|")


# --------------------------------------------------------------------------- #
# Docstring parsing
# --------------------------------------------------------------------------- #
def _parse_doc(doc: Optional[str], synopsis: str) -> Tuple[str, Dict[str, str]]:
    """Split a command docstring into (description prose, {section: body}).

    Tolerates both normalised (real ``\\x08`` no-wrap markers) and un-normalised
    (literal ``\\b``) docstrings.
    """
    if not doc:
        return "", {}

    # Drop the Click no-wrap marker lines entirely (real ``\x08`` after normalisation,
    # or a literal ``\b`` left over from un-normalised docstrings), so they leave no
    # stray blank lines. Genuine blank lines between blocks are preserved.
    lines = []
    for raw in doc.split("\n"):
        unmarked = raw.replace("\x08", "")
        if raw != unmarked and not unmarked.strip():
            continue
        if unmarked.strip() == "\\b":
            continue
        lines.append(unmarked)

    # Drop a leading summary line that merely duplicates the synopsis.
    while lines and not lines[0].strip():
        lines.pop(0)
    if lines and lines[0].strip().rstrip(".").lower() == synopsis.strip().rstrip(".").lower():
        lines.pop(0)

    pre: List[str] = []
    sections: Dict[str, List[str]] = {}
    current: Optional[str] = None
    for line in lines:
        match = _SECTION_RE.match(line.strip()) if line == line.strip() else None
        if match:
            label = match.group(1)
            label = "Examples" if label.startswith("Example") else label
            current = label
            sections.setdefault(current, [])
            continue
        if current is None:
            pre.append(line)
        else:
            sections[current].append(line)

    description = "\n".join(pre).strip()
    cleaned = {k: textwrap.dedent("\n".join(v)).strip("\n") for k, v in sections.items()}
    return description, cleaned


def _render_section(label: str, body: str, command_path: List[str]) -> str:
    if label == "Examples":
        return f"## Examples\n\n```bash\n{body}\n```\n"
    if label == "URI Format":
        return f"## URI Format\n\n```text\n{body}\n```\n"
    if label == "Arguments":
        rows = _kv_rows(body)
        if rows:
            out = ["## Arguments\n", "| Argument | Description |", "|----------|-------------|"]
            out += [f"| `{name}` | {_cell(desc)} |" for name, desc in rows]
            return "\n".join(out) + "\n"
        return f"## Arguments\n\n{body}\n"
    if label == "See Also":
        rows = _kv_rows(body)
        if rows:
            out = ["## See also\n"]
            out += [
                f"- {_link_command(name, command_path)} — {desc}" if desc else f"- {_link_command(name, command_path)}"
                for name, desc in rows
            ]
            return "\n".join(out) + "\n"
        return f"## See also\n\n{body}\n"
    if label == "Notes":
        return f"## Notes\n\n{body}\n"
    return f"## {label}\n\n{body}\n"


def _kv_rows(body: str) -> List[Tuple[str, str]]:
    """Parse ``NAME: description`` lines (folding hanging-indent continuations)."""
    rows: List[Tuple[str, str]] = []
    for raw in body.split("\n"):
        if not raw.strip():
            continue
        if ":" in raw and not raw.startswith((" ", "\t")):
            name, _, desc = raw.partition(":")
            rows.append((name.strip(), desc.strip()))
        elif rows:
            prev_name, prev_desc = rows[-1]
            rows[-1] = (prev_name, (prev_desc + " " + raw.strip()).strip())
    return rows


def _link_command(spec: str, from_path: List[str]) -> str:
    """Turn a ``See Also`` command spec into a relative link if it resolves."""
    parts = spec.strip().split()
    if parts and parts[0] == "copick":
        parts = parts[1:]
    target = _page_path_for(parts)
    if target is None:
        return f"`{spec.strip()}`"
    from_page = _page_path_for(from_path)
    rel = os.path.relpath(target, os.path.dirname(from_page))
    return f"[`copick {' '.join(parts)}`]({rel})"


# --------------------------------------------------------------------------- #
# Page-path / tree helpers
# --------------------------------------------------------------------------- #
_LEAF_PATHS: Dict[Tuple[str, ...], bool] = {}  # full path -> is_group


def _page_path_for(path: List[str]) -> Optional[str]:
    key = tuple(path)
    if key not in _LEAF_PATHS:
        return None
    if _LEAF_PATHS[key]:  # group
        return "docs/cli/" + "/".join(path) + "/index.md"
    return "docs/cli/" + "/".join(path) + ".md" if path else "docs/cli/index.md"


def _editorial_path_for(path: List[str], is_group: bool) -> str:
    """Repo-root-relative path of the hand-edited editorial partial for a command.

    Lives under ``docs/cli_editorial/`` (resolved by the ``.`` snippets base_path).
    """
    if not path:
        return "docs/cli_editorial/index.md"
    if is_group:
        return "docs/cli_editorial/" + "/".join(path) + "/index.md"
    return "docs/cli_editorial/" + "/".join(path) + ".md"


def _editorial_include(path: List[str], is_group: bool) -> str:
    """A snippets include line for the editorial partial, but only if it exists.

    Partials are never seeded empty: a command's page picks one up as soon as an
    author creates ``docs/cli_editorial/<.../command>.md`` and regenerates.
    """
    rel = _editorial_path_for(path, is_group)
    if os.path.exists(rel):
        return f'--8<-- "{rel}"\n'
    return ""


def _index_tree(ctx: click.Context) -> None:
    """Pre-compute the path->is_group map (needed for cross-page See-Also links)."""
    _LEAF_PATHS.clear()

    def walk(grp: click.Group, path: List[str]) -> None:
        for name in grp.list_commands(ctx):
            cmd = grp.get_command(ctx, name)
            if cmd is None or cmd.hidden:
                continue
            full = path + [name]
            _LEAF_PATHS[tuple(full)] = _is_group(cmd)
            if _is_group(cmd):
                walk(cmd, full)

    walk(cli, [])


# --------------------------------------------------------------------------- #
# Page rendering
# --------------------------------------------------------------------------- #
def _usage_line(cmd: click.Command, path: List[str], ctx: click.Context) -> str:
    parts = ["copick", *path, cmd.options_metavar or "[OPTIONS]"]
    if _is_group(cmd):
        parts.append("COMMAND [ARGS]...")
    else:
        for param in cmd.params:
            if isinstance(param, click.Argument):
                metavar = _arg_metavar(cmd, param, ctx)
                parts.append(metavar if param.required else f"[{metavar}]")
    return " ".join(p for p in parts if p)


def _option_tables(cmd: click.Command) -> str:
    """Render option tables, one per click_option_group section."""
    sections: "Dict[str, List[click.Parameter]]" = {}
    blurbs: Dict[str, str] = {}
    order: List[str] = []

    for param in cmd.params:
        if isinstance(param, click.Argument) or getattr(param, "hidden", False):
            continue
        group = _group_of(param)
        section = group.name.strip() if group is not None else "Options"
        if _is_group_title(param):
            blurbs[section] = param.help or ""
            continue
        if section not in sections:
            sections[section] = []
            order.append(section)
        sections[section].append(param)

    # "Options" (ungrouped) always renders first.
    if "Options" in order:
        order.remove("Options")
        order.insert(0, "Options")

    if not any(sections.values()):
        return ""

    out = ["## Options\n"]
    for section in order:
        params = sections.get(section, [])
        if not params:
            continue
        if section != "Options":
            out.append(f"### {section}\n")
        if blurbs.get(section):
            out.append(f"*{blurbs[section]}*\n")
        out.append("| Option | Type | Default | Description |")
        out.append("|--------|------|---------|-------------|")
        for param in params:
            out.append(
                f"| {_opt_names(param)} | {_type_label(param)} | {_default_label(param)} | {_cell(param.help)} |",
            )
        out.append("")
    return "\n".join(out).rstrip() + "\n"


def _render_leaf(cmd: click.Command, path: List[str], ctx: click.Context) -> str:
    synopsis = cmd.short_help or cmd.get_short_help_str() or "No description available."
    description, sections = _parse_doc(inspect.getdoc(cmd.callback) if cmd.callback else None, synopsis)

    blocks = [AUTOGEN_HEADER, f"# copick {' '.join(path)}\n", f"*{synopsis}*\n"]

    if getattr(cmd, "deprecated", False):
        msg = cmd.deprecated if isinstance(cmd.deprecated, str) else "This command is deprecated."
        blocks.append(f'!!! warning "Deprecated"\n    {msg}\n')

    blocks.append(f"## Usage\n\n```bash\n{_usage_line(cmd, path, ctx)}\n```\n")

    if description:
        blocks.append(f"## Description\n\n{description}\n")

    if "Arguments" in sections:
        blocks.append(_render_section("Arguments", sections["Arguments"], path))
    if "URI Format" in sections:
        blocks.append(_render_section("URI Format", sections["URI Format"], path))

    options = _option_tables(cmd)
    if options:
        blocks.append(options)

    for label in ("Examples", "See Also", "Notes"):
        if label in sections:
            blocks.append(_render_section(label, sections[label], path))

    blocks.append(_editorial_include(path, is_group=False))
    return "\n".join(b for b in blocks if b).rstrip() + "\n"


def _render_group(grp: click.Group, path: List[str], ctx: click.Context) -> str:
    title = "copick " + " ".join(path) if path else "copick CLI"
    synopsis = grp.short_help or grp.get_short_help_str() or ""
    description, sections = _parse_doc(inspect.getdoc(grp.callback) if grp.callback else None, synopsis)

    blocks = [AUTOGEN_HEADER, f"# {title}\n"]
    if synopsis:
        blocks.append(f"*{synopsis}*\n")
    blocks.append(f"## Usage\n\n```bash\n{_usage_line(grp, path, ctx)}\n```\n")
    if description:
        blocks.append(f"## Description\n\n{description}\n")

    rows = ["## Commands\n", "| Command | Description |", "|---------|-------------|"]
    for name in grp.list_commands(ctx):
        cmd = grp.get_command(ctx, name)
        if cmd is None or cmd.hidden:
            continue
        link = f"{name}/index.md" if _is_group(cmd) else f"{name}.md"
        desc = cmd.short_help or cmd.get_short_help_str() or ""
        rows.append(f"| [`{name}`]({link}) | {_cell(desc)} |")
    blocks.append("\n".join(rows) + "\n")

    blocks.append(_editorial_include(path, is_group=True))
    return "\n".join(b for b in blocks if b).rstrip() + "\n"


def _render_index(ctx: click.Context) -> str:
    """The CLI reference landing page: a category-grouped command index."""
    blocks = [AUTOGEN_HEADER, "# CLI Reference\n", "*The complete `copick` command-line interface.*\n"]

    top = {name: cli.get_command(ctx, name) for name in cli.list_commands(ctx)}
    top = {k: v for k, v in top.items() if v is not None and not v.hidden}
    seen = set()
    for category, names in COMMAND_CATEGORIES.items():
        present = [n for n in sorted(names) if n in top]
        if not present:
            continue
        seen.update(present)
        rows = [f"## {category}\n", "| Command | Description |", "|---------|-------------|"]
        for name in present:
            cmd = top[name]
            link = f"{name}/index.md" if _is_group(cmd) else f"{name}.md"
            desc = cmd.short_help or cmd.get_short_help_str() or ""
            rows.append(f"| [`{name}`]({link}) | {_cell(desc)} |")
        blocks.append("\n".join(rows) + "\n")

    other = [n for n in sorted(top) if n not in seen]
    if other:
        rows = ["## Other Commands\n", "| Command | Description |", "|---------|-------------|"]
        for name in other:
            cmd = top[name]
            link = f"{name}/index.md" if _is_group(cmd) else f"{name}.md"
            desc = cmd.short_help or cmd.get_short_help_str() or ""
            rows.append(f"| [`{name}`]({link}) | {_cell(desc)} |")
        blocks.append("\n".join(rows) + "\n")

    # Editorial guides (env-var tip, plugin system, common workflows) live below the
    # command index so the navigation stays at the top of the page.
    blocks.append(_editorial_include([], is_group=False))

    return "\n".join(b for b in blocks if b).rstrip() + "\n"


# --------------------------------------------------------------------------- #
# Generation driver
# --------------------------------------------------------------------------- #
def _generate_pages(outdir: str) -> Dict[str, str]:
    """Return {page_path: content} for all CLI pages."""
    ctx = click.Context(cli, info_name="copick")
    _index_tree(ctx)

    pages: Dict[str, str] = {}
    pages[os.path.join(outdir, "index.md")] = _render_index(ctx)

    def walk(grp: click.Group, path: List[str]) -> None:
        for name in grp.list_commands(ctx):
            cmd = grp.get_command(ctx, name)
            if cmd is None or cmd.hidden:
                continue
            full = path + [name]
            if _is_group(cmd):
                pages[os.path.join(outdir, *full, "index.md")] = _render_group(cmd, full, ctx)
                walk(cmd, full)
            else:
                pages[os.path.join(outdir, *full[:-1], f"{name}.md")] = _render_leaf(cmd, full, ctx)

    walk(cli, [])
    return pages


